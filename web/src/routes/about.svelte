<div>
	<h1>Liar</h1>
	<h2>Game Rules</h2>
	<p>
		Liar is an interpretation of Liar's Dice,
		a dice game consisting of rolling dice and
		betting on the number of faces rolled without
		being called out!
	</p>
	<p>
		The game starts with all the
		players having a number of dice, usually 5.
		They all roll at the start of a round, only
		knowing the result of their own dice.
		Then the players bids on a face and a quantity.
		With each player either raising the bid,
		or challenging the previous player's bid.
		If the number of dice is equal or higher than that
		of the bid, the challenger loses.
		If there are less, the bidder loses.
		When someone loses, they lose one dice and a new round begins.
		When someone loses all their dice, they are out of the game.
		The game continues until there is only one player left
		on the board.
	</p>
	<h2>Wild ones</h2>
	<p>
		Wild ones make the face "one" a wild card, making it count
		to any bid.
	</p>
	
</div>

